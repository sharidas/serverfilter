namespace App\Entity;


class FormData
{
    protected $storage;
    protected $ram;
    protected $hdisk;
    protected $location;
    protected $data;
    protected $uploadFile;

    /**
     * @return mixed
     */
    public function getStorage()
    {
        return $this->storage;
    }

    /**
     * @param mixed $storage
     */
    public function setStorage($storage): void
    {
        $this->storage = $storage;
    }

    /**
     * @return mixed
     */
    public function getRam()
    {
        return $this->ram;
    }

    /**
     * @param mixed $ram
     */
    public function setRam($ram): void
    {
        $this->ram = $ram;
    }

    /**
     * @return mixed
     */
    public function getHdisk()
    {
        return $this->hdisk;
    }

    /**
     * @param mixed $hdisk
     */
    public function setHdisk($hdisk): void
    {
        $this->hdisk = $hdisk;
    }

    /**
     * @return mixed
     */
    public function getLocation()
    {
        return $this->location;
    }

    /**
     * @param mixed $location
     */
    public function setLocation($location): void
    {
        $this->location = $location;
    }

    /**
     * @return mixed
     */
    public function getData()
    {
        return $this->data;
    }

    /**
     * @param mixed $data
     */
    public function setData($data): void
    {
        $this->data = $data;
    }

    /**
     * @return mixed
     */
    public function getUploadFile()
    {
        return $this->uploadFile;
    }

    /**
     * @param mixed $uploadFile
     */
    public function setUploadFile($uploadFile): void
    {
        $this->uploadFile = $uploadFile;
    }
}
